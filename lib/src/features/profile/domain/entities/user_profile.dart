class UserProfile {
  final int id;
  final int age;
  final String gender;
  final double weight;
  final double height;
  final String experienceLevel;
  final String goal;

  UserProfile({
    required this.id,
    required this.age,
    required this.gender,
    required this.weight,
    required this.height,
    required this.experienceLevel,
    required this.goal,
  });
}
