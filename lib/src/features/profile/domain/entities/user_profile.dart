class UserProfile {
  final int id;
  final int age;
  final String gender;
  final double weight;
  final double height;
  final String experienceLevel;
  final String goal;
  final double targetWeight;
  final double targetBodyFat;
  final double targetNeck;
  final double targetShoulders;
  final double targetChest;
  final double targetAbdomen;
  final double targetWaist;
  final double targetGlutes;
  final double targetThigh;
  final double targetCalf;
  final double targetArm;
  final double targetForearm;

  UserProfile({
    required this.id,
    required this.age,
    required this.gender,
    required this.weight,
    required this.height,
    required this.experienceLevel,
    required this.goal,
    required this.targetWeight,
    required this.targetBodyFat,
    required this.targetNeck,
    required this.targetShoulders,
    required this.targetChest,
    required this.targetAbdomen,
    required this.targetWaist,
    required this.targetGlutes,
    required this.targetThigh,
    required this.targetCalf,
    required this.targetArm,
    required this.targetForearm,
  });
}
